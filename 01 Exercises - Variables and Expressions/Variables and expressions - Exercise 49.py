# 49. Implemente um programa que calcule o ano de nascimento de uma pessoa a partir de sua idade e do ano atual.

idade_atual = int(input("Digite a sua idade: "))
ano_atual = int(input("Digite o ano atual: "))
nascimento = ano_atual - idade_atual
print(f"O seu ano de nascimento é: {nascimento}")


















